export { SORDELL_QUESTIONS };

export function useSordell({ expert, niche, personalStories, sordellAnswers, setSordellAnswers, setSordellResult, setSordellStep, setSordellError, setSordellLoading, setExpandedTopics, setExpandingTopic, expandedTopics }) {

  async function generateSordellResult() {
    setSordellLoading(true); setSordellError("");
    const qa = SORDELL_QUESTIONS.map((q,i) => `Вопрос ${i+1}: ${q.q}\nОтвет: ${sordellAnswers[i]||"-"}`).join("\n\n");
    const prompt = `Ты контент-стратег по личным брендам. Проанализируй ответы эксперта.
Эксперт: ${expert||"-"}. Ниша: ${niche||"-"}.
ОТВЕТЫ: ${qa}
Найди 10 сильных тем. Для каждой: хук до 12 слов, квадрант Сорделл, топ-3 (top:true + reason).
ТОЛЬКО JSON: {"topics":[{"n":1,"topic":"тема","hook":"хук","quadrant":"Личное + Неожиданное","top":false,"reason":""}]}`;
    try {
      const resp = await fetch("/api/claude", { method:"POST", headers:{"Content-Type":"application/json"}, body:JSON.stringify({ model:"claude-haiku-4-5-20251001", max_tokens:4000, messages:[{role:"user",content:prompt}] }) });
      const data = await resp.json();
      if (data.error) throw new Error(data.error.message);
      const text = data.content.map(b=>b.text||"").join("");
      const parsed = JSON.parse(text.replace(/```json|```/g,"").trim());
      setSordellResult(parsed.topics);
      localStorage.setItem("lia_sordell_result", JSON.stringify(parsed.topics));
      localStorage.setItem("lia_sordell_answers", JSON.stringify(sordellAnswers));
      setSordellStep(13);
    } catch(e) { setSordellError("Ошибка: " + e.message); }
    setSordellLoading(false);
  }

  return { generateSordellResult, SORDELL_QUESTIONS };
}
